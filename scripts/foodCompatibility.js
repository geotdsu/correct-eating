function checkCompatibility() {
    const food1 = getSelectValue1();
    const food2 = getSelectValue2();

    const result = compatibilityTable[food1]?.[food2];

    const resultBtn = document.getElementById("resultBtn")

    resultBtn.classList.remove("good-button", "normal-button", "bad-button");


    if (result) {
        resultBtn.textContent = result;

        if (result === "კარგი") {
            resultBtn.classList.add("good-button");
        } else if (result === "დასაშვები") {
            resultBtn.classList.add("normal-button");
        } else if (result === "ცუდი") {
            resultBtn.classList.add("bad-button");
        }
    } 
}

document.getElementById("food1").addEventListener("change", checkCompatibility);
